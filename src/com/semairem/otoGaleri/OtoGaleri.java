package com.semairem.otoGaleri;

public class OtoGaleri {
	private String aracTuru;
	private String marka;
	private String modeli;
	private String aracTipi;
	private String yakiti;
	private int id;
	private String durumu;
	
	public String getAracTipi() {
		return aracTipi;
	}
	public void setAracTipi(String aracTipi) {
		this.aracTipi = aracTipi;
	}
	
	public String getAracTuru() {
		return aracTuru;
	}
	public void setAracTuru(String aracTuru) {
		this.aracTuru = aracTuru;
	}
	
	public String getMarkasi() {
		return marka;
	}
	public void setMarka(String marka) {
		this.marka = marka;
	}
	
	public String getModeli() {
		return modeli;
	}
	public void setModeli(String modeli) {
		this.modeli = modeli;
	}
	
	public String getYakiti() {
		return yakiti;
	}
	public void setYakiti(String yakiti) {
		this.yakiti = yakiti;
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

	public String getDurumu() {
		return durumu;
	}
	public void setDurumu(String durumu) {
		this.durumu = durumu;
}
	

}
